namespace StageKit;

/// <summary>
/// Identifies which side of a collection should be trimmed.
/// </summary>
public enum CollectionSide
{
    /// <summary>
    /// Trim items from the beginning of the collection.
    /// </summary>
    Head,

    /// <summary>
    /// Trim items from the end of the collection.
    /// </summary>
    Tail
}
